function convertToDirectUrl(url) {
  if (!url) return '';

  // Google Drive: convierte cualquier formato de Drive a enlace directo
  // Formato: https://drive.google.com/file/d/ID/view  →  usa ID
  // Formato: https://drive.google.com/open?id=ID
  // Formato: https://drive.google.com/uc?id=ID
  let gdMatch = url.match(/drive\.google\.com\/file\/d\/([a-zA-Z0-9_-]+)/);
  if (!gdMatch) gdMatch = url.match(/[?&]id=([a-zA-Z0-9_-]+)/);
  if (gdMatch) {
    return 'https://drive.google.com/uc?export=download&confirm=t&id=' + gdMatch[1];
  }

  // Dropbox: cambia dl=0 a dl=1 y raw=0 a raw=1
  if (url.includes('dropbox.com')) {
    return url.replace('dl=0','dl=1').replace('raw=0','raw=1').replace('www.dropbox','dl.dropboxusercontent');
  }

  // Ya es un enlace directo
  return url;
}

function updateMusicUI() {
  const name = bookData.music.name || 'Música de fondo';
  document.getElementById('music-title-display').textContent = name;
  if (bookData.music.url) {
    const direct = convertToDirectUrl(bookData.music.url);
    audio.src = direct;
    audio.load();
  }
}

function toggleMusic() {
  if (!bookData.music.url) {
    document.getElementById('music-title-display').textContent = 'Sin música configurada';
    return;
  }

  if (musicPlaying) {
    audio.pause();
    musicPlaying = false;
    document.getElementById('play-btn').textContent = '♪';
    document.querySelectorAll('.eq-bar').forEach(b => b.classList.add('paused'));
  } else {
    // Recarga la URL por si acaso
    if (!audio.src || audio.src === window.location.href) {
      updateMusicUI();
    }
    const playPromise = audio.play();
    if (playPromise !== undefined) {
      playPromise.then(() => {
        musicPlaying = true;
        document.getElementById('play-btn').textContent = '■';
        document.querySelectorAll('.eq-bar').forEach(b => b.classList.remove('paused'));
      }).catch(err => {
        console.warn('Error al reproducir:', err);
        document.getElementById('music-title-display').textContent = '⚠ Clic para activar';
      });
    }
  }
}

audio.addEventListener('error', function() {
  document.getElementById('music-title-display').textContent = '⚠ Error al cargar audio';
  musicPlaying = false;
  document.getElementById('play-btn').textContent = '♪';
});


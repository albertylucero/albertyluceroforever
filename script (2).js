function renderChapter(index) {
  const ch = bookData.chapters[index];
  const content = document.getElementById('chapter-content');
  content.className = 'fade-in';

  const canRead = isAdmin || ch.status === 'published';

  let bodyHtml = '';
  if (canRead && ch.content.trim()) {
    const paragraphs = ch.content.split(/\n\n+/).filter(p => p.trim());
    bodyHtml = `<div class="chapter-body">${paragraphs.map(p => `<p>${p.replace(/\n/g, '<br>')}</p>`).join('')}</div>`;
  } else if (canRead && !ch.content.trim()) {
    bodyHtml = `<div class="chapter-placeholder"><span class="lock-icon">✍️</span>${isAdmin ? 'Este capítulo aún no tiene contenido. <br>Usa el panel de autor para escribirlo.' : 'El autor está escribiendo este capítulo...<br><em>Vuelve pronto.</em>'}</div>`;
  } else {
    bodyHtml = `<div class="chapter-placeholder"><span class="lock-icon">🔒</span>Este capítulo aún no ha sido publicado.<br><em>El autor lo está preparando con amor para ti.</em></div>`;
  }

  content.innerHTML = `
    <div class="chapter-header">
      <div class="chapter-number">✦ Capítulo ${index + 1} ✦</div>
      <h1 class="chapter-title">${ch.title}</h1>
    </div>
    ${bodyHtml}
  `;

  document.getElementById('prev-btn').disabled = index === 0;
  document.getElementById('next-btn').disabled = index === bookData.chapters.length - 1;
}

